function allocateCounts(total, weights) {
  const totalWeight = weights.reduce((sum, row) => sum + row.exam * 0.7 + row.guide * 0.3, 0);
  const weighted = weights.map((row) => {
    const raw = total * (row.exam * 0.7 + row.guide * 0.3) / totalWeight;
    return { ...row, raw, count: Math.floor(raw) };
  });
  let remaining = total - weighted.reduce((sum, row) => sum + row.count, 0);
  weighted
    .slice()
    .sort((a, b) => (b.raw - b.count) - (a.raw - a.count))
    .slice(0, remaining)
    .forEach((row) => {
      const target = weighted.find((item) => item.unit === row.unit);
      target.count += 1;
    });
  return weighted;
}

function applyAllocatedCounts(units, weights) {
  const allocated = allocateCounts(1000, weights);
  units.forEach((unit) => {
    const target = allocated.find((row) => row.unit === unit.unit);
    if (!target) {
      throw new Error(`No weight configured for ${unit.unit}`);
    }
    unit.count = target.count;
  });
}


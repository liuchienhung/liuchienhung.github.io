function pickUnitDistractors(unit, concept, index, globalIndex) {
  const pool = unit.concepts.filter((candidate) => candidate[0] !== concept[0]);
  const picked = [];
  let cursor = (index * 5 + globalIndex) % pool.length;
  while (picked.length < 3) {
    const candidate = pool[cursor % pool.length];
    if (!picked.some((item) => item[0] === candidate[0])) {
      picked.push(candidate);
    }
    cursor += 7;
  }
  return {
    concepts: picked,
    options: picked.map((candidate, pickedIndex) => formatOption(unit, candidate, index + globalIndex + pickedIndex * 5))
  };
}

